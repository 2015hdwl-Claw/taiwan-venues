// 更新資料
const updatedData = data.map(venue => {
  const oldName = venue.name;
  
  // 檢查是否需要更新名稱
  for (const [chinese, english] of Object.entries(venueAbbreviations)) {
    if (oldName.includes(chinese) && !oldName.includes('(')) {
      // 替換為新格式：中文名稱(英文簡稱)
      venue.name = oldName.replace(chinese, `${chinese}(${english})`);
      break;
    }
  }
  
  return venue;
});

// 儲存更新後的資料
fs.writeFileSync('venues-all-cities.json', JSON.stringify(updatedData, null, 2), 'utf8');

